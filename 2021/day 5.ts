const createGrid = (input) => {
  const grid = [];
  let i = 1;
  while (i < 1000) {
    grid.push(Array(1000).fill(0));
    i++;
  }
  return grid;
};

const determineDangerSpots = (grid) => {
  for (let pair of input) {
    const [left, right] = pair;
    if (left.x !== right.x && left.y !== right.y) {
      const bothLeftLower = left.x < right.x && left.y < right.y;
      const bothRightLower = left.x > right.x && left.y > right.y;

      if (bothLeftLower) {
        for (let i = 0; i <= Math.abs(right.x - left.x); i++) {
          grid[left.x + i][left.y + i]++;
        }
        continue;
      }
      if (bothRightLower) {
        for (let i = 0; i <= Math.abs(left.x - right.x); i++) {
          grid[right.x + i][right.y + i]++;
        }
        continue;
      }

      if (left.x > right.x) {
        for (let i = 0; i <= Math.abs(left.x - right.x); i++) {
          grid[left.x - i][left.y + i]++;
        }
      } else if (right.x > left.x) {
        for (let i = 0; i <= Math.abs(right.x - left.x); i++) {
          grid[left.x + i][left.y - i]++;
        }
      }
    }

    if (left.y == right.y) {
      const lower = Math.min(left.x, right.x);
      let i = 0;
      while (i <= Math.abs(left.x - right.x)) {
        grid[lower + i][left.y]++;

        i++;
      }
    }

    if (left.x == right.x) {
      const lower = Math.min(left.y, right.y);
      let i = 0;
      while (i <= Math.abs(left.y - right.y)) {
        grid[left.x][lower + i]++;

        i++;
      }
    }
  }
  return grid;
};

const countDangerSpots = (grid) => {
  let total = 0;

  for (let item of grid.flat(2)) {
    if (item > 1) {
      total++;
    }
  }
  return total;
};

